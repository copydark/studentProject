    public static void resetTable(){
        clear();
        tablePages = Integer.parseInt(jTextFieldPage.getText());
        tableNumOfRecords = Integer.parseInt(jTextFieldRecord.getText());
        DefaultTableModel dtm2 = (DefaultTableModel)jTable1.getModel();
        dtm2.setDataVector(null, new String [] {
                "CourseID", "Course Name", "Active", "Credit", "Area", "Department Name", "Prerequirement", "Description"
            });
        String key = jTextFieldKeyword.getText();
        String department = jComboBoxKeyDepart.getModel().getSelectedItem().toString();
        int departmentID = CourseData.getDepartID(department);
        String courseArea = jComboBoxKeyArea.getModel().getSelectedItem().toString();

        CourseTable.fillCourseJtable(dtm2, key, departmentID, courseArea, jTable1.getColumnCount(), tableNumOfRecords, tablePages);
        jLabelTotalNumPag.setText("/"+CourseTable.totalPages());
    }
    
    private void setDepartmentList(){
        String[] departmentList = CourseData.getDepartmentList();
        for(int i = 0; i<departmentList.length; i++){
            jComboBoxDepartment.addItem(departmentList[i]);
            jComboBoxKeyDepart.addItem(departmentList[i]);
        }
    }
        
