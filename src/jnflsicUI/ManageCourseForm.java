    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        rowIndex = jTable1.getSelectedRow();
        jTextFieldCourseID.setText(model.getValueAt(rowIndex, 0).toString());
        jTextFieldCourseName.setText(model.getValueAt(rowIndex, 1).toString());
        jCheckBoxCourseActive.setSelected(false);
        if(model.getValueAt(rowIndex, 2).toString().equals("Open")){
            jCheckBoxCourseActive.setSelected(true);
        }else{
            jCheckBoxCourseActive.setSelected(false);
        }
        int credit = Integer.parseInt(model.getValueAt(rowIndex, 3).toString());
        jComboBoxCredit.getModel().setSelectedItem(credit);
        
        String area = model.getValueAt(rowIndex, 4).toString();
        if(area.equals("")){
            area = "N/A";
        }
        jComboBoxArea.getModel().setSelectedItem(area);
        String department = "N/A";
        if(model.getValueAt(rowIndex, 5)==null){
            department = "N/A";
        }else{
            department = model.getValueAt(rowIndex, 5).toString();
        }
//        String department = model.getValueAt(rowIndex, 5).toString();
//        System.out.println(department);
//        if(department.equals("")){
//            department = "N/A";
//        }
        jComboBoxDepartment.getModel().setSelectedItem(department);
        jTextFieldPrerequirement.setText(model.getValueAt(rowIndex, 6).toString());
        //System.out.println(model.getValueAt(rowIndex, 6).toString());
        String desc = model.getValueAt(rowIndex, 7).toString();
        if(desc==null){
            desc = "";
        }
        jTexCourseDescription.setText(desc);
        
    }//GEN-LAST:event_jTable1MouseClicked

    private void jTable1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyReleased
        if(evt.getKeyCode()==KeyEvent.VK_UP || evt.getKeyCode()==KeyEvent.VK_DOWN){
            rowIndex = jTable1.getSelectedRow();
            rowIndex = jTable1.getSelectedRow();
            jTextFieldCourseID.setText(model.getValueAt(rowIndex, 0).toString());
            jTextFieldCourseName.setText(model.getValueAt(rowIndex, 1).toString());
            jCheckBoxCourseActive.setSelected(false);
            if(model.getValueAt(rowIndex, 2).toString().equals("Open")){
                jCheckBoxCourseActive.setSelected(true);
            }else{
                jCheckBoxCourseActive.setSelected(false);
            }
            int credit = Integer.parseInt(model.getValueAt(rowIndex, 3).toString());
            jComboBoxCredit.getModel().setSelectedItem(credit);
            jComboBoxArea.getModel().setSelectedItem(model.getValueAt(rowIndex, 4).toString());
            jComboBoxDepartment.getModel().setSelectedItem(model.getValueAt(rowIndex, 5).toString());
            jTextFieldPrerequirement.setText(model.getValueAt(rowIndex, 6).toString());
            jTexCourseDescription.setText(model.getValueAt(rowIndex, 7).toString());
        }
    }//GEN-LAST:event_jTable1KeyReleased

